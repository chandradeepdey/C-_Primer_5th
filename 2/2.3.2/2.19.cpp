// pointers are objects themselves, they contain the address to other
// objects.
// references are just names, not objects. they are aliases to other objects.

// pointers can be left uninitialized, references must always be initialized.

// pointers can be made to point to a different location. references cannot alias
// a different object post initialization.

// since pointers are objects, we may take pointers to pointers and references to
// pointers. references are not objects, hence their addresses may not be taken.
